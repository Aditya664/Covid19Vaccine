    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       int i = jTable1.getSelectedRow();
        if (i == -1) {
            Messages.showWarning("Please select Row", "select row");
        } else {
            int ans = JOptionPane.showConfirmDialog(this, "Do you want to delete?", "Confirm",JOptionPane.YES_NO_OPTION);
            if(ans == JOptionPane.YES_OPTION){
                String id=jTable1.getValueAt(i, 0)+"";
            db.execute("delete from taltbl where id=?", id);
            Messages.showInfo("tal Deleted", "Deleted");
            }else{
                return;
            }
            
        }
        db.fillTable(jTable1,"select * from taltbl");
    }//GEN-LAST:event_jButton2ActionPerformed

