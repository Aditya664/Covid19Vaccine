    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int id = 0;
        String name = talname.getText();
        if (name.isEmpty()) {
            Messages.showWarning("Name is empty", "Error");
        } else if (!Rules.isChar(name)) {
            Messages.showWarning("Format is incorrect", "Error");
        } else {
            ResultSet rs = db.getData("select * from taltbl where name=?", name);
            try {
                if (rs.next()) {
                    Messages.showError("Taluka already exist", "Error");
                } else {
                    db.execute("insert into taltbl values(?,?)", id, name);
                    Messages.showInfo("Talukha inserted", "success");
                    db.fillTable(jTable1, "select * from taltbl");
                }
                db.fillTable(jTable1, "select * from taltbl");
            } catch (SQLException ex) {
                Logger.getLogger(AddTal.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String name = talname.getText();
        int i = jTable1.getSelectedRow();
        if(i == -1){
            Messages.showWarning("Please select Row", "select row");
        }else{
            String id = jTable1.getValueAt(i, 0)+"";
            db.execute("update taltbl set name=? where id=?", name,id);
            Messages.showInfo("tal Updated", "success");
        }
        db.fillTable(jTable1, "select * from taltbl");
    }//GEN-LAST:event_jButton3ActionPerformed

